//! AWS SDK response to OutputData conversion utilities
//!
//! This module provides utilities to convert AWS CloudFormation SDK responses
//! and internal iidy data structures to OutputData for the data-driven output system.

use crate::{
    aws::client_req_token::{TokenInfo as TimingTokenInfo, TokenSource as TimingTokenSource},
    cfn::{CfnContext, StackArgs},
    cli::NormalizedAwsOpts,
    output::{
        CommandMetadata, CommandResult, OutputData, StackDefinition, StackEvent,
        StackEventWithTiming, StackEventsDisplay, StackExportInfo, StackListColumn,
        StackListDisplay, StackListEntry, StackOutputInfo, StackResourceInfo, StatusLevel,
        StatusUpdate, TokenInfo as OutputTokenInfo, TokenSource as OutputTokenSource,
        data::{ErrorDetails, ErrorInfo},
    },
};
use aws_sdk_cloudformation::types::{Output, Stack, StackEvent as AwsStackEvent, StackResource};
use chrono::{DateTime, Utc};
use std::collections::HashMap;

fn convert_aws_datetime(aws_dt: &aws_smithy_types::DateTime) -> DateTime<Utc> {
    DateTime::from_timestamp(aws_dt.as_secs_f64().floor() as i64, 0).unwrap_or_else(Utc::now)
}

/// Convert aws::client_req_token::TokenInfo to output::TokenInfo
pub fn convert_token_info(timing_token: &TimingTokenInfo) -> OutputTokenInfo {
    let source = match &timing_token.source {
        TimingTokenSource::UserProvided => OutputTokenSource::UserProvided,
        TimingTokenSource::AutoGenerated => OutputTokenSource::AutoGenerated,
        TimingTokenSource::Derived { from, step } => OutputTokenSource::Derived {
            from: from.clone(),
            step: step.clone(),
        },
    };

    OutputTokenInfo {
        value: timing_token.value.clone(),
        source,
        operation_id: timing_token.operation_id.clone(),
    }
}

/// Convert CfnContext and operation info to CommandMetadata
pub async fn create_command_metadata(
    context: &CfnContext,
    opts: &NormalizedAwsOpts,
    stack_args: &StackArgs,
    environment: &str,
) -> Result<CommandMetadata, anyhow::Error> {
    let mut cli_arguments = HashMap::new();

    // Add CLI arguments from opts
    if let Some(ref profile) = opts.profile {
        cli_arguments.insert("profile".to_string(), profile.clone());
    }
    if let Some(ref region) = opts.region {
        cli_arguments.insert("region".to_string(), region.clone());
    }

    // Always include the primary token, but renderers will decide whether to display it
    let primary_token = convert_token_info(context.primary_token());

    // Only include derived tokens if we actually derived new ones (not just the primary)
    let all_tokens = context.get_used_tokens();
    let derived_tokens = if all_tokens.len() > 1 {
        all_tokens
            .into_iter()
            .skip(1)
            .map(|t| convert_token_info(&t))
            .collect()
    } else {
        vec![]
    };

    // Get current IAM principal
    let current_iam_principal = get_current_iam_principal(context).await?;

    Ok(CommandMetadata {
        iidy_environment: environment.to_string(),
        region: context
            .aws_config
            .region()
            .expect("Region must be configured - validated in create_context_for_operation")
            .as_ref()
            .to_string(),
        profile: opts.profile.clone(),
        cli_arguments,
        iam_service_role: stack_args.role_arn.clone(),
        current_iam_principal,
        credential_source: context.credential_sources.display_name(),
        iidy_version: env!("CARGO_PKG_VERSION").to_string(),
        primary_token,
        derived_tokens,
    })
}

/// Get the current IAM principal from AWS STS
async fn get_current_iam_principal(context: &CfnContext) -> Result<String, anyhow::Error> {
    let (_, arn) = get_caller_identity(context).await?;
    Ok(arn)
}

/// Get AWS account ID and IAM principal ARN from STS GetCallerIdentity
pub async fn get_caller_identity(context: &CfnContext) -> Result<(String, String), anyhow::Error> {
    let sts_client = aws_sdk_sts::Client::new(&context.aws_config);

    match sts_client.get_caller_identity().send().await {
        Ok(response) => {
            let account = response.account().unwrap_or("unknown").to_string();
            let arn = response
                .arn()
                .unwrap_or("arn:aws:iam::unknown:user/current")
                .to_string();
            Ok((account, arn))
        }
        Err(e) => {
            // Fall back to placeholders if STS call fails (e.g., no internet, permissions issue)
            eprintln!("Warning: Could not get caller identity: {e}");
            Ok((
                "unknown".to_string(),
                "arn:aws:iam::unknown:user/current".to_string(),
            ))
        }
    }
}

/// Create a status update from a message
pub fn create_status_update(message: &str, level: StatusLevel) -> OutputData {
    OutputData::StatusUpdate(StatusUpdate {
        message: message.to_string(),
        timestamp: Utc::now(),
        level,
    })
}

/// Create a command result for operation completion
pub fn create_command_result(
    success: bool,
    elapsed_seconds: i64,
    message: Option<String>,
) -> OutputData {
    OutputData::CommandResult(CommandResult {
        success,
        elapsed_seconds: elapsed_seconds.max(0) as u64, // Convert to u64, ensuring non-negative
        message,
        exit_code: if success { 0 } else { 1 },
    })
}

/// Create a final command summary (exact iidy-js showFinalComandSummary pattern)
pub fn create_final_command_summary(success: bool, elapsed_seconds: i64) -> OutputData {
    use crate::output::data::{CommandSummaryResult, FinalCommandSummary};

    let result = if success {
        CommandSummaryResult::Success
    } else {
        CommandSummaryResult::Failure
    };

    OutputData::FinalCommandSummary(FinalCommandSummary {
        result,
        elapsed_seconds,
    })
}

