    #[test]
    fn test_duration_calculation() {
        let base_time = "2023-07-10T14:00:00Z".parse::<DateTime<Utc>>().unwrap();

        let events = vec![
            StackEvent {
                event_id: "1".to_string(),
                stack_id: "test-stack".to_string(),
                stack_name: "test-stack".to_string(),
                logical_resource_id: "MyResource".to_string(),
                physical_resource_id: Some("resource-123".to_string()),
                resource_type: "AWS::S3::Bucket".to_string(),
                timestamp: Some(base_time),
                resource_status: "CREATE_IN_PROGRESS".to_string(),
                resource_status_reason: None,
                resource_properties: None,
                client_request_token: None,
            },
            StackEvent {
                event_id: "2".to_string(),
                stack_id: "test-stack".to_string(),
                stack_name: "test-stack".to_string(),
                logical_resource_id: "MyResource".to_string(),
                physical_resource_id: Some("resource-123".to_string()),
                resource_type: "AWS::S3::Bucket".to_string(),
                timestamp: Some(base_time + chrono::Duration::seconds(5)),
                resource_status: "CREATE_COMPLETE".to_string(),
                resource_status_reason: None,
                resource_properties: None,
                client_request_token: None,
            },
        ];

        let events_with_timing = calculate_event_durations(events);

        // First event (IN_PROGRESS) should have no duration
        assert_eq!(events_with_timing[0].duration_seconds, None);

        // Second event (COMPLETE) should have 5 second duration
        assert_eq!(events_with_timing[1].duration_seconds, Some(5));
    }
