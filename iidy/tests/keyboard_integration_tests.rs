//! Keyboard Integration Tests
//!
//! Tests the keyboard listener integration with DynamicOutputManager for
//! dynamic mode switching during CloudFormation operations.

use iidy::output::{
    DynamicOutputManager, KeyboardListener, KeyboardConfig, KeyboardCommand,
    OutputMode, CommandMetadata, TokenInfo, TokenSource, StatusUpdate, StatusLevel
};
use iidy::output::manager::OutputOptions;
use chrono::Utc;
use std::collections::HashMap;
use std::time::Duration;
use tokio;

/// Helper function to create test output options
fn create_test_output_options() -> OutputOptions {
    OutputOptions::minimal()
}

/// Helper function to create sample command metadata
fn create_sample_command_metadata() -> CommandMetadata {
    CommandMetadata {
        iidy_environment: "test".to_string(),
        region: "us-east-1".to_string(),
        profile: Some("test-profile".to_string()),
        cli_arguments: HashMap::new(),
        iam_service_role: None,
        current_iam_principal: "arn:aws:iam::123456789012:user/test-user".to_string(),
        iidy_version: "2.0.0".to_string(),
        primary_token: TokenInfo {
            value: "test-token-001".to_string(),
            source: TokenSource::AutoGenerated,
            operation_id: "create-stack-001".to_string(),
        },
        derived_tokens: vec![],
    }
}

#[tokio::test]
async fn test_keyboard_listener_tty_detection() {
    // Test that keyboard listener respects TTY detection
    let config = KeyboardConfig::default();
    let listener = KeyboardListener::new(config);
    
    // The enabled state should match TTY detection
    assert_eq!(listener.is_enabled(), iidy::output::keyboard::is_tty_environment());
}

#[tokio::test]
async fn test_keyboard_listener_disabled_in_non_tty() {
    // Create a keyboard listener that's explicitly disabled (simulates non-TTY)
    let config = KeyboardConfig {
        enabled: false,
        poll_interval: Duration::from_millis(50),
        show_help_on_start: false,
    };
    
    let mut listener = KeyboardListener::new(config);
    assert!(!listener.is_enabled());
    
    // All operations should be no-ops
    assert!(listener.start().await.is_ok());
    assert!(listener.try_recv_command().is_none());
    assert!(listener.show_help().await.is_ok());
    assert!(listener.stop().await.is_ok());
}

#[tokio::test]
async fn test_keyboard_commands_mapping() {
    // Test that keyboard commands map correctly to output modes
    assert_eq!(
        KeyboardListener::command_to_output_mode(&KeyboardCommand::SwitchToPlain),
        Some(OutputMode::Plain)
    );
    assert_eq!(
        KeyboardListener::command_to_output_mode(&KeyboardCommand::SwitchToInteractive),
        Some(OutputMode::Interactive)
    );
    assert_eq!(
        KeyboardListener::command_to_output_mode(&KeyboardCommand::SwitchToJson),
        Some(OutputMode::Json)
    );
    
    // Non-mode commands should return None
    assert_eq!(
        KeyboardListener::command_to_output_mode(&KeyboardCommand::ShowHelp),
        None
    );
    assert_eq!(
        KeyboardListener::command_to_output_mode(&KeyboardCommand::Quit),
        None
    );
    assert_eq!(
        KeyboardListener::command_to_output_mode(&KeyboardCommand::ToggleTimestamps),
        None
    );
}

#[tokio::test]
async fn test_integration_with_output_manager() {
    // Test integration between keyboard listener and output manager
    let output_options = create_test_output_options();
    let mut output_manager = DynamicOutputManager::new(OutputMode::Plain, output_options).await
        .expect("Should create output manager");
    
    // Create a disabled keyboard listener for testing
    let keyboard_config = KeyboardConfig {
        enabled: false,
        poll_interval: Duration::from_millis(50),
        show_help_on_start: false,
    };
    let mut keyboard_listener = KeyboardListener::new(keyboard_config);
    
    // Verify initial state
    assert_eq!(output_manager.current_mode(), OutputMode::Plain);
    assert!(!keyboard_listener.is_enabled());
    
    // Test the integration function with disabled keyboard
    let continue_processing = iidy::output::keyboard::handle_keyboard_commands(
        &mut keyboard_listener,
        &mut output_manager
    ).await.expect("Should handle keyboard commands");
    
    // Should continue processing when keyboard is disabled
    assert!(continue_processing);
    assert_eq!(output_manager.current_mode(), OutputMode::Plain);
}

