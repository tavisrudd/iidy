#[tokio::test]
async fn test_keyboard_listener_error_handling() {
    // Test that keyboard listener handles errors gracefully
    let config = KeyboardConfig {
        enabled: false, // Disabled to prevent terminal interference
        poll_interval: Duration::from_millis(50),
        show_help_on_start: false,
    };
    
    let mut listener = KeyboardListener::new(config);
    
    // All operations should succeed even when disabled
    assert!(listener.start().await.is_ok());
    assert!(listener.show_help().await.is_ok());
    assert!(listener.stop().await.is_ok());
    
    // Multiple start/stop cycles should be safe
    assert!(listener.start().await.is_ok());
    assert!(listener.start().await.is_ok()); // Should be idempotent
    assert!(listener.stop().await.is_ok());
    assert!(listener.stop().await.is_ok()); // Should be idempotent
}