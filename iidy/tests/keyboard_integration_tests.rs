#[tokio::test]
async fn test_end_to_end_mode_switching_simulation() {
    // Simulate a complete CloudFormation operation with mode switching
    let output_options = create_test_output_options();
    let mut output_manager = DynamicOutputManager::new(OutputMode::Interactive, output_options).await
        .expect("Should create output manager");
    
    // Render some initial data
    let metadata = create_sample_command_metadata();
    output_manager.render(iidy::output::OutputData::CommandMetadata(metadata)).await
        .expect("Should render initial metadata");
    
    assert_eq!(output_manager.current_mode(), OutputMode::Interactive);
    assert_eq!(output_manager.buffer_len(), 1);
    
    // Simulate mode switches (without actual keyboard input)
    output_manager.switch_to_mode(OutputMode::Plain).await
        .expect("Should switch to Plain mode");
    assert_eq!(output_manager.current_mode(), OutputMode::Plain);
    
    output_manager.switch_to_mode(OutputMode::Json).await
        .expect("Should switch to JSON mode");
    assert_eq!(output_manager.current_mode(), OutputMode::Json);
    
    // Add more data after mode switches
    let status = StatusUpdate {
        message: "Operation continuing".to_string(),
        timestamp: Utc::now(),
        level: StatusLevel::Info,
    };
    output_manager.render(iidy::output::OutputData::StatusUpdate(status)).await
        .expect("Should render status after mode switch");
    
    // Verify event history is maintained
    assert!(output_manager.buffer_len() >= 2);
    
    // Switch back to Interactive mode to test full cycle
    output_manager.switch_to_mode(OutputMode::Interactive).await
        .expect("Should switch back to Interactive mode");
    assert_eq!(output_manager.current_mode(), OutputMode::Interactive);
}

#[test]
fn test_keyboard_config_validation() {
    // Test that keyboard configuration respects TTY environment
    let default_config = KeyboardConfig::default();
    assert_eq!(default_config.enabled, iidy::output::keyboard::is_tty_environment());
    assert_eq!(default_config.poll_interval, Duration::from_millis(50));
    assert!(!default_config.show_help_on_start);
    
    // Test explicit configuration
    let custom_config = KeyboardConfig {
        enabled: true,
        poll_interval: Duration::from_millis(100),
        show_help_on_start: true,
    };
    assert!(custom_config.enabled);
    assert_eq!(custom_config.poll_interval, Duration::from_millis(100));
    assert!(custom_config.show_help_on_start);
}

